#提供列表实参，返回元素乘积
def multiplyList(myList) :
     
    result = 1
    for x in myList:
         result = result * x  
    return result

#提供列表与单位长度两个实参，返回所有以单位长度为元素长度的所有相邻数字的列表
def xianglinshuzi(ls, unit):
	waiting_for_multiply = []
	
	for x in ls:
	
		if len(x) == unit:
			waiting_for_multiply.append(x)
	
		else:
			for y, z in zip(range(0,len(x)-unit+1-1), range(unit,len(x))):
				waiting_for_multiply.append(x[y:z])
	
		waiting_for_multiply.append(x[-unit:])

	return waiting_for_multiply


